const continents = [];

function filterNationality(e){
    if(!continents.includes(e.value)){
        continents.push(e.value)
    }
    else{
        continents.splice(continents.findIndex((item) => {return item === e.value}), 1)
    }
    // console.log(continents)

    const tableRows = document.querySelectorAll('tbody tr');

    tableRows.forEach(row => {
        if(continents.length === 0){
            row.classList.remove("d-none")
        }else{
            for(let k in countries){
                if(continents.includes(k)){
                    if(countries[k].includes(row.lastElementChild.innerHTML)){
                        row.classList.remove("d-none")
                        break;
                    }
                }
                else{
                    row.classList.add("d-none")
                }
            }
        }
    });
}

